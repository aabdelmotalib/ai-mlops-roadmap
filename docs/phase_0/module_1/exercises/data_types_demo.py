# This script demonstrates all the basic data types in Python
# STRING: Text data
name = 'Ahmed Abdelmoteleb'
message = 'Welcome to Python!'
print(f'Name: {name}')
print(f'Message {message}')

# INTEGER: Whole numbers
age = 28
count = 0
diff = 500 - 250
print(f'Age: {age}')
print(f'Count: {count}')
print(f'Difference: {diff}')
# FLOAT: Numbers with decimals
height = 5.9
price = 160.25
pi_value = 3.14159
print(f'Hight: {height}')
print(f'Price: {price}')
print(f'PI: {pi_value}')
# BOOLEAN: True or False
is_student = True
is_raining = False
print(f'Is Student: {is_student}')
print(f'Is Raining: {is_raining}')
# LIST: Multiple items in order
fruits = ['apple', 'banana', 'orange']
print(f'Fruits: {fruits}')
print(f'The First One: {fruits[0]}')
print(f'Number of fruits {len(fruits)}')
# DICTIONARY: Key-value pairs (like labels and values)
person = {
    'name': 'Ahmed Abdelmoteleb',
    'age': 28,
    'city': 'Cairo'
}
print(f'Person: {person}')
print(f'Person name: {person["name"]}')
print(f'Person age: {person["age"]}')

